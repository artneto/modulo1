# Lendo a distância em quilômetros
DISTANCIA = int(input())

# Calculando o tempo necessário para o carro Y alcançar a distância em relação ao carro X
# O carro Y se distancia 1 quilômetro a cada 2 minutos
# Então, o tempo necessário é a distância multiplicada por 2
tempoNecessario = DISTANCIA * 2

# Imprimindo o tempo necessário seguido da mensagem "minutos"
print(tempoNecessario, "MINUTOS")
